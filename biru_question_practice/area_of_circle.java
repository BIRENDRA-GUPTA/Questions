public class area_of_circle{
    public static void main(String[] args) {
        // area of circle
        int r = 5;
        System.out.println("area of circle " + 3.14*r*r);

        // perimeter of circle
        System.out.println("perimeter of circle " + 2*3.14*r);

        // area of rectangle
        System.out.println("area of rectangle " + r*r);
    }
}